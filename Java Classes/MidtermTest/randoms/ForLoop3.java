package randoms;

public class ForLoop3 {

	public static void main(String[] args) {
		for (int I = 0;I < 2;I++) {
			switch(I) {
			case 0:
				System.out.println("I is " + I);
				break;
			case 1:
				System.out.println("I is " + I);
				break;
			}
		}
		System.out.println("Out of for loop");
	}

}
