package randoms;

public class ForLoop5 {

	public static void main(String[] args) {
	       for(int i = 1;i < 5;i++)
	       {
	    	   switch(i % 3)
	    	   {
	    	   default:
	    		   System.out.print("pear ");
	    		   break;
	    	   case 2:
	    		   System.out.print("orange ");
	    		   break;	    		   
	    	   case 1:
	    		   System.out.print("apple ");
	    		   break;	    		   
	    		   
	    	   }
	       }

	}

}
